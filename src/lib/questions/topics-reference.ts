// - Interpretação Textual: questão pede objetivo, intenção, propósito, significado, infere, deduz, tema, mensagem
// - Gramática: questão sobre estruturas linguísticas, verbos, pronomes, concordância, regência, crase
// - Vocabulário: questão sobre significado de palavras, sinônimos, antônimos, expressões
// - Variação Linguística: questão sobre dialects, registros, linguagem formal/informal, gírias
// - Funções de Linguagem: questão sobre funções comunicativas (referencial, emotiva, conativa, etc.)
// - Gênero Textual: questão sobre tipo de texto, estrutura, propósito comunicativo
// - Literatura: questão sobre obras, autores, movimentos literários, análise de poemas/textos literários
// - Produção Textual: questão sobre escrita, coesão, coerência, argumentação
